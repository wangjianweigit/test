public class OrderProductSku {
	/** 商品skuid */
	private long sku_id;
	/** sku旧价格 */
	private double order_old_prize;
	/** sku新价格 */
	private double order_new_prize;
	/** sku优惠价格 */
	private double order_discount_money;
	public long getSku_id() {
		return sku_id;
	}
	public void setSku_id(long sku_id) {
		this.sku_id = sku_id;
	}
	public double getOrder_old_prize() {
		return order_old_prize;
	}
	public void setOrder_old_prize(double order_old_prize) {
		this.order_old_prize = order_old_prize;
	}
	public double getOrder_new_prize() {
		return order_new_prize;
	}
	public void setOrder_new_prize(double order_new_prize) {
		this.order_new_prize = order_new_prize;
	}
	public double getOrder_discount_money() {
		return order_discount_money;
	}
	public void setOrder_discount_money(double order_discount_money) {
		this.order_discount_money = order_discount_money;
	}
	
}
