package com.tk.oms.product.entity;

/**
 * 商品操作日志实体类
 *
 * @author zhenghui
 * @version 1.00
 * @date 2019/05/15
 */
public class ProductOperationLog {

    /**日志ID*/
    private long id;
    /**商品货号*/
    private String productItemnumber;
    /**业务类型*/
    private int operationType;
    /**业务名称*/
    private String operationName;
    /**日志类型*/
    private int keywordType;
    /**日志名称*/
    private String keywordName;
    /**所操作的业务ID*/
    private long businessId;
    /**所操作的业务名称*/
    private String businessName;
    /**备注说明*/
    private String remark;
    /**操作人ID*/
    private long createUserId;
    /**操作人所属平台*/
    private long operatorPlatform;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getProductItemnumber() {
        return productItemnumber;
    }

    public void setProductItemnumber(String productItemnumber) {
        this.productItemnumber = productItemnumber;
    }

    public int getOperationType() {
        return operationType;
    }

    public void setOperationType(int operationType) {
        this.operationType = operationType;
    }

    public String getOperationName() {
        return operationName;
    }

    public void setOperationName(String operationName) {
        this.operationName = operationName;
    }

    public int getKeywordType() {
        return keywordType;
    }

    public void setKeywordType(int keywordType) {
        this.keywordType = keywordType;
    }

    public String getKeywordName() {
        return keywordName;
    }

    public void setKeywordName(String keywordName) {
        this.keywordName = keywordName;
    }

    public long getBusinessId() {
        return businessId;
    }

    public void setBusinessId(long businessId) {
        this.businessId = businessId;
    }

    public String getBusinessName() {
        return businessName;
    }

    public void setBusinessName(String businessName) {
        this.businessName = businessName;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public long getCreateUserId() {
        return createUserId;
    }

    public void setCreateUserId(long createUserId) {
        this.createUserId = createUserId;
    }
    
    public long getOperatorPlatform() {
		return operatorPlatform;
	}

	public void setOperatorPlatform(long operatorPlatform) {
		this.operatorPlatform = operatorPlatform;
	}
}
