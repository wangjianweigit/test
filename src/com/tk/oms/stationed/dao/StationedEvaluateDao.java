package com.tk.oms.stationed.dao;

import org.springframework.stereotype.Repository;

import java.util.List;
import java.util.Map;

@Repository
public interface StationedEvaluateDao {

    /**
     * 查询评估表列表数据
     * @param params
     * @return
     */
    List<Map<String, Object>> queryListForPage(Map<String, Object> params);


    /**
     * 查询评估表数据总数
     * @param params
     * @return
     */
    int queryCountForPage(Map<String, Object> params);


    /**
     * 获取详情列表
     * @param params
     * @return
     */
    List<Map<String, Object>> queryDetailList(Map<String, Object> params);


    /**
     * 获取详情数量
     * @param params
     * @return
     */
     int queryDetailCount(Map<String, Object> params);

}
